package com.diyshirt.to;
import java.util.Date;

public class PictureTo {
	private int PicID;
	private String PicName;
	private String UserID;
	private short PicCate;
	private String PicPath;
	private Date PicTime;
	private short Status;
	
	public void setPicID(int PicID){
		this.PicID = PicID;
	}
	public int getPicID(){
		return this.PicID;
	}
	
	public void setPicName(String PicName){
		this.PicName = PicName;
	}
	public String getPicName(){
		return this.PicName;
	}
	
	public void setUserID(String UserID){
		this.UserID = UserID;
	}
	public String getUserID(){
		return this.UserID;
	}
	
	public void setPicCate(short PicCate){
		this.PicCate = PicCate;
	}
	public short getPicCate(){
		return this.PicCate;
	}
	
	public void setPicPath(String PicPath){
		this.PicPath = PicPath;
	}
	public String getPicPath(){
		return this.PicPath;
	}
	
	public void setPicTime(Date PicTime){
		this.PicTime = PicTime;
	}
	public Date getPicTime(){
		return PicTime;
	}
	
	public void setStatus(short Status){
		this.Status = Status;
	}
	public short getStatus(){
		return this.Status;
	}
}
